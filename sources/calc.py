def add2(arg1, arg2):
    if (arg1>0 and arg2>0): 
        return arg1+ arg2
    return "Wrong Input"
def sub2(arg1, arg2):
    if (arg1>0 and arg2>0): 
        return arg1-arg2
    return "Wrong Input"

def pro2(arg1, arg2):
    if (arg1>0 and arg2>0): 
        return arg1*arg2
    return "Wrong Input"
